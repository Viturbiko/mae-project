		<h1>Med Atlatnic Ecobonus</h1>

		<hr align="left" width="10%">

		<p><b>Med Atlantic Ecobonus (MAE)</b> Action is a policy study co-financed by the European Union performing a comprehensive analysis on a possible approach to eco-incentives schemes to stimulate sustainable freight transport services in the
current TEN-t framework. The proposed approach follows a regional perspective, combines support from Member States and the EU through common principles, is opened to all EU regions and modes of transport and is based on grants (eco-incentives) which reflect the actual external cost savings attained through specific actions in market conditions, instead of being exclusively referred to the financial needs of the actions.</p>

		<p>The main goal of the approach is to accelerate the market decision towards those actions and practices which could contribute most to reduce external costs in freight transport, particularly those reducing carbon emissions, air pollutants and socio-economic impacts.</p>

