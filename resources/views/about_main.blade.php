				<p>With the CEF Regulation under revision for the next period and the TEN-t policy to be starting the revision process in the short term, it seems the right time for the debate on possible approaches to tacklethese socio-environmental issues coming in the next years while securing the conditions for the further transport market development and competitiveness.</p>
				
				<p>In this context, the Med Atlantic Ecobonus (MAE) Action is an institutional study co-financed by the European Union with this main goals:</p>

				<ol>
					<li>To set up a debate on a possible common EU approach on eco-incentive schemes to stimulate a sustainable freight transport market behavior. The approach is intended to be transferable to all modes of transport and EU regions. If broad consensus were to be achieved, these eco-incentive schemes could become a part of the EU funding priorities, as eligible actions within the next Multiannual Financial Framework 2021-2027 (i.e. CEF2).</li>
					<br>
					<li>To perform a complete ex-ante analysis, taken the case of the Motorways of the Sea (MoS) as example to prove the impacts of the proposed approach (MAE example).</li>
				</ol>

				<p>The MAE common EU approach -open to the debate- stablishes the following main principles that schemes shall comply to get the EU funding support:</p>

				<ul>
					<li>Targeted to mature markets. Not intended to start-up services, to compensate market losses or to pure modal shift goals (different approach from Marco Polo programmes)</li>
					<li>Stimulating a sustainable freight transport market behavior</li>
					<li>Incentive calculation based exclusively on demonstrated socio-environmental merits reducing negative externalities in transport (i.e. measuring external costs savings is needed based on common references -EC’s Handbook of Transport External Costs-)</li>
					<li>Technologically agnostic on how the socio-environmental merit is achieved</li>
					<li>With the funding being conditioned to results</li>
					<li>Demonstrating performance achieved (i.e. monitoring)</li>
					<li>With the MS being the promoters of the schemes (regional approach) and those who mobilize and implement the necessary budget (as with the national schemes) provided there is EU co-financing.</li>
				</ul>

				<p>In addition to these principles, granting any financial support shall be conditional to an “ex-ante” analysis showing in advance the impacts of the scheme to assess the impacts of the eco-incentive measure in the targeted market. The simulation must be carried out with the use of relevant tools based on common references, developed ad hoc for the targeted market and limited through eligibility criteria. As a result, the “ex-ante” analysis shall demonstrate the impacts of the scheme and the budgetary needs.</p>

