				<h2>Following this approach, the main features of the MAE example are:</h2>
				<ul>
					<li>Targeted Market: MoS servicing alternative routes to the road transport in the West-Mediterranean and Atlantic regions.</li> 
					<li>Goal: Triggering green actions in the maritime leg (improving environmental performance of vessels)</li>
					<li>Socio-Environmental Merit incentivized: The external cost savings from a truck using a maritime service compared to the ‘road-only’ route.</li> 
					<li>The road transport receives the eco-incentive when using the maritime service. The eco-incentive is directed through demand (Italian ecobonus approach)</li> 
					<li>Only for maritime services adopting green actions (technology or not technology based)</li> 
					<li>An external cost calculator tool has been developed specifically for the targeted market to measure the environmental merit which is incentivized, as described. This calculator is based on the latest update available of the EC’ Handbook of External Costs.</li> 
				</ul>

				<p>For more information on the ex-ante results please check the Executive Report on the <a href="{{ route('downloads') }}">downloads</a> section.</p>

