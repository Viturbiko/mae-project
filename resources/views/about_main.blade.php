				<h1>Objectives of</h1>
				<h2>integration, sustainability and greening technologies in transport</h2>
				<p>Due to the constant increase of international trade over the last thirty years the European Union (EU) has been faced with a number of social and environmental challenges addressing the negative impacts of freight mobility. In this context, the need to foster a sustainable freight transport market behavior reducing the external costs of transport has become a major goal, firmly addressed in the EU policy.</p>
				<p>From today’s perspective, and partly due to a smart combination of regulatory and financial measures performing together as a sort of “stick and carrot” mechanism, the transport market is performing better than in the past in many aspects.</p>
				<p>However, there are plenty socio-environmental challenges ahead for all modes of transport. Decarbonisation is now in the upfront of the international agenda. For the first time, the maritime sector is facing real targets for CO2 emissions reduction after the recent adoption by the International Maritime Organization of the initial strategy on decarbonization in shipping. As well, the insufficient contribution of road transport to overall CO2 emissions reduction is on the debate within legal framework for charging heavy goods vehicles, currently under revision. Air pollutant reduction targets are also getting stricter from the environmental regulation and extended to all modes of transport. In particular, this is a short-term challenge for the maritime industry by 2020 with the entry into force of the new Sulphur cap on marine fuels, whereas road transport has already made great improvements following the implementation of the EURO class regulation. However, road transport generates a number of social impacts, related mainly to congestion and accidents, that are still to be reduced.</p>

